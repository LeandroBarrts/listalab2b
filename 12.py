import numpy as np

# Usando linspace para criar um array com 50 valores igualmente espaçados entre 0 e 10
array_linspace = np.linspace(0, 10, 50)

# Usando arange para criar um array com valores de 0 a 10, incrementando de 0.2
array_arange = np.arange(0, 10.2, 0.2)

# Exibindo os resultados
print("Array com linspace (50 valores igualmente espaçados entre 0 e 10):")
print(array_linspace)

print("\nArray com arange (valores de 0 a 10, incrementando de 0.2):")
print(array_arange)
